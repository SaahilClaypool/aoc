    record Command()
    {
        public static Command Parse(string line) =>
            line == "noop" ? 
                new Noop() :
                new Add(int.Parse(line.Split(" ")[1]));
        public record Noop() : Command;
        public record Add(int Num) : Command;
    }
    public override string SolveA(string input)
    {
        var registerByCycle = RunMachine(input);
        Log(string.Join("\n", registerByCycle));
        Log($"Cycles: {registerByCycle.Count}");
        return Enumerable.Range(0, 6)
            .Select(n => n * 40 + 20)
            .Select(cycle => registerByCycle[cycle] * cycle)
            .Sum()
            .ToString();
    }

    private static List<int> RunMachine(string input)
    {
        var rx = 1;
        var registerByCycle = new List<int>() { rx };
        foreach (var command in input.Lines().Select(Command.Parse))
        {
            if (command is Command.Add { Num: var num })
            {
                registerByCycle.Add(rx);
                registerByCycle.Add(rx);
                rx += num;
            }
            else
            {
                registerByCycle.Add(rx);
            }
        }

        return registerByCycle;
    }

    public override string SolveB(string input)
    {
        var output = new StringBuilder();
        var registerByCycle = RunMachine(input);
        foreach (var row in Enumerable.Range(0, 6))
        {
            foreach (var col in Enumerable.Range(0, 40))
            {
                var cycle = row * 40 + col + 1;
                var shouldDraw = Math.Abs(registerByCycle[cycle] - col) < 2;
                if (shouldDraw)
                {
                    output.Append('#');
                }
                else
                {
                    output.Append('.');
                }
                Log($"""
                cycle {cycle}: register: {registerByCycle[cycle]} col: {col}
                {shouldDraw}
                line: {output}
                """);
            }
            output.Append('\n');
        }
        return output.ToString().Trim();
    }

